package com.napier.sem;

/**
 * Represents a population report which we use for population reports.
 * @author logan
 */
public class Pop_Report
{
    /**
     * Location of Report
     */
    public String location;

    /**
     * Population of Location
     */
    public int population;

    /**
     * Population in City
     */
    public float urban_pop;

    /**
     * Population Outside City
     */
    public float rural_pop;

}